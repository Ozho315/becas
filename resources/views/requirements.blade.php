    </div>

    <div class="w-full text-sm text-gray-600 dark:text-gray-400">
        El estudiante que postule para el otorgamiento de beca, deberá registrar la
        información requerida según el tipo de beca, en formato pdf, dentro del portafolio estudiantil.
        <br>
        1. RENDIMIENTO ACADÉMICO:
        <br>
        a) Solicitud dirigida al Presidente de la Comisión de Calificación y Aprobación de Becas;
        <br>
        b) Cédula de ciudadanía o de identidad vigente y papeleta de votación;
        <br>
        c) Promoción del último período académico;
        <br>
        d) Certificado de no tener segunda o tercera matrícula;
        <br>
        e) Tener un promedio igual o superior a 8,5 para las unidades académicas FICA y
        FICAYA y un promedio de 9 a 10 para las unidades académicas FECYT, FCS,
        FACAE;
        <br>
        f) Certificado de estar legalmente matriculado, en al menos tres asignaturas;
        <br>
        g) Certificado actualizado de no encontrarse afiliado al IESS, (Seguro General).
        <br>
        <br>
        2. DISCAPACIDAD:
        <br>
        a) Solicitud dirigida al Presidente de la Comisión de Calificación y Aprobación de Becas;
        <br>
        b) Cédula de ciudadanía o de identidad vigente;
        <br>
        c) Promoción del último período académico;
        <br>
        d) Certificado de estar legalmente matriculado, en al menos tres asignaturas;
        <br>
        e) Certificado actualizado de no encontrarse afiliado al IESS, (seguro general);
        <br>
        f) En caso de no tener registrado en la cédula de ciudadanía la discapacidad, presentar
        el carné emitido por CONADIS o Ministerio de Salud Pública.
        <br>
        <br>
        3. CONDICIÓN ECONÓMICA VULNERABLE:
        <br>
        a) Solicitud dirigida al Presidente de la Comisión de Calificación y Aprobación de Becas:
        <br>
        b) Cédula de ciudadanía o de identidad vigente y papeleta de votación;
        <br>
        c) Promoción del último período académico;
        <br>
        d) Certificado de no tener segunda o tercera matrícula;
        <br>
        e) Certificado actualizado de no encontrarse afiliado al IESS, (Seguro General);
        <br>
        f) Certificado de estar legalmente matriculado, en al menos tres asignaturas;
        <br>
        g) Rol de pagos legalizado de los padres cuando sea en relación de dependencia y/o
        rol de pensión de jubilado;
        <br>
        En caso de no contar con los documentos habilitantes del literal g, presentar la ficha
        socioeconómica;
        <br>
        La justificación de ingresos propios o familiares no podrán superar el monto equivalente a 1.5 SBU vigente al año de aplicación a la beca.
        <br>
        <br>
        CONDICIONES DE ETNIA, EXTRANJEROS CON ESTATUS DE REFUGIADO Y ECUATORIANOS MIGRANTES RETORNADOS O DEPORTADOS:
        <br>
        a) Solicitud dirigida al Presidente de la Comisión de Calificación y Aprobación de Becas;
        <br>
        b) Documento de identificación:
        <br>
        b.1.- Cédula de ciudadanía o de identidad vigente y papeleta de votación.
        b.2.- Visa de protección internacional.
        b.3.- Certificado de Migrante retornado.
        <br>
        c) Promoción del último periodo académico;
        <br>
        d) Certificado de no tener segunda o tercera matrícula;
        <br>
        e) Certificado actualizado de no encontrarse afiliado al IESS, (Seguro General);
        <br>
        f) Certificado de estar legalmente matriculado, en al menos tres asignaturas;
        <br>
        g) Pertenecer a los grupos históricamente excluidos: indígena, afrodescendiente, montubios registrado en la ficha socioeconómica SIIU;
        <br>
        <br>
        PERTENECER A CLUBES ACADÉMICOS Y GRUPOS ARTÍSTICOS, CULTURALES Y DEPORTIVOS, REPRESENTANDO A LA UTN O AL PAÍS.
        <br>
        a) Solicitud dirigida al Presidente de la Comisión de Calificación y Aprobación de Becas;
        <br>
        b) Cédula de ciudadanía o de identidad vigente y papeleta de votación;
        <br>
        c) Promoción del último periodo académico;
        <br>
        d) Certificado de no tener segunda o tercera matrícula;
        <br>
        e) Certificado de estar legalmente matriculado, en al menos tres asignaturas;
        <br>
        f) Certificado actualizado de no encontrarse afiliado al IESS (Seguro General),
        <br>
        g) Certificado emitido por el Director de Vinculación, Director CUDIC y/o Decano de Unidad Académica, en el que justifique la participación del estudiante durante al menos los últimos seis meses en estas actividades representando a la Universidad o al país.
