        En la Universidad Técnica del Norte se establece los siguientes porcentajes para
        la asignación de becas:
        a) Por condición económica vulnerable, el 55% de becas.
        <br>
        b) Por rendimiento académico, el 30% de becas.
        <br>
        c) Por condiciones de etnia, extranjeros con estatus de refugiado y ecuatorianos
        migrantes retornados o deportados, el 8% de becas.
        <br>
        d) Por pertenecer a clubes académicos y grupos artísticos, culturales y deportivos,
        representando a la Universidad Técnica del Norte o al país, el 5% de becas.
        <br>
        e) Estudiantes con discapacidades 2% de becas.
        <br>
        <br>
        En caso de que no se cumplan los porcentajes establecidos, estos se podrán rever previo al
        estudio y análisis de la Comisión de Calificación y Aprobación de Becas
    </div>

